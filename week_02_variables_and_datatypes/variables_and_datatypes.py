# Variables
# Variables are used to store values
# Variables are created when a value is assigned to it
# Variables do not need to be declared with any particular type and can even change type after they have been set
# Variable names are case sensitive
# Variable names can be any length
# Variable names can contain letters, numbers, and the underscore character
# Variable names cannot start with a number
# Variable names cannot contain spaces
# Variable names cannot contain special characters
# Variable names should be descriptive, without being too long

# Variable assignment
x = 5
y = True
z = 'Hello World'

