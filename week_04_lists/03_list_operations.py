# '*' operator is supported for lists - Union of given lists
list_01 = [1, 2, 3, 4, 5]
list_02 = [6, 7, 8, 9, 10]
list_03 = list_01 + list_02
print(list_01 + list_02)
print(list_03)

# '-' operator is not supported for lists

# '*' operator is supported for lists - repetition of given lists
print(list_01 * 5)

